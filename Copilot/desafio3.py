numb1 = int(input('Digite o primeiro número: '));
numb2 = int(input('Digite o segundo número: '));

operacao = input('Operacao desejada: (+, -, *, /)')

if operacao == '+':
    print(numb1 + numb2);

elif operacao == '-':
    print(numb1 - numb2);

elif operacao == '*':
    print(numb1 * numb2);

elif operacao == '/':
    print(numb1 / numb2);